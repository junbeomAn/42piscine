#include <unistd.h>

int		n_to_p(int n)
{
	if (n < 0)
	{
		return (-n);
	}
	return (n);
}

void	ft_putnbr(int nb)
{
	char c;

	if (nb < 10 && nb > -10)
	{
		c = n_to_p(nb % 10) + '0';
		if (nb < 0)
			write(1, "-", 1);
		write(1, &c, 1);
		return ;
	}
	ft_putnbr(nb / 10);
	c = n_to_p(nb % 10) + '0';
	write(1, &c, 1);
}
